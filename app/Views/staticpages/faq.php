                                                    <div class="wpb_text_column wpb_content_element  wpb_animate_when_almost_visible wpb_fadeIn fadeIn">
                                                        <div class="wpb_wrapper">
                                                            <p>VPE is structured to allow any company with a legitimate connection to the propane industry to participate and be listed on the site at low or no cost to the exhibitor, services provider, or propane media outlet. For the LAUNCH, VPE is providing the <strong>“Virtual Freedom Package” Free to the first 275 exhibitors</strong>. Due to the desire to help fellow propane exhibitors/suppliers we have extended the free booth package from the first 150 exhibitors now to the first 275. Sign up quick as these free slots are getting filled fast.</p>
                                                            <p>Premium services, generally referred to as the “Partner/Sponsor” program, including but not limited to, premium placement in the Virtual Exhibit Floor, placement in VPE eblasts and Press Releases are available for a <strong>one-time fee for a designated Virtual Trade Show and the marketing associated with that Virtual Trade Show</strong> which will normally occur on a specific date with marketing prior to and after the one day event.</p>
                                                            <p>Other premium services are expected to be forthcoming, however the ‘regular’ placement on the site is discounted from $500 to FREE (for first 275 exhibitors).</p>
                                                            <p>As of April 10, 2020, <strong>Premium or other paid services are specific to a single online trade show, typically one day long</strong>, with marketing that leads up to the event and follows on afterwards for a month and includes:</p>
                                                            <ul>
                                                                <li>Virtual Booth space, aka “Booth space”</li>
                                                                <li>Premium page placement, ie placement in premier exhibitor section</li>
                                                                <li>Premium placement on home screen as premier exhibitor</li>
                                                                <li>Premium placement in eblasts</li>
                                                                <li>Premium placement in Press Releases and/or inclusion in an industry article as available</li>
                                                                <li>Able to work with your design team to place marketing materials specific to your design needs</li>
                                                                <li>Ability to post button links to your videos, brochures, company site, calendar, &amp; lead capture pages.</li>
                                                            </ul>
                                                            <p>Premium benefits for Partner/Sponsorship is valid for the day of the show, the days leading up to that show and for a month after that show.</p>

