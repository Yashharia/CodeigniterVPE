                                                <span class="mkdf-tab-title">Why was the Virtual Propane Expo (VPE) created?</span>
                                            </h5>
                                            <div class="mkdf-accordion-content">
                                                <div class="mkdf-accordion-content-inner">

                                                    <div class="wpb_text_column wpb_content_element ">
                                                        <div class="wpb_wrapper">
                                                            <p>The VPE was created by propane industry leaders in the wake of the Coronavirus outbreak of early 2020 as a solution to help bring the propane industry together. Specifically, we felt the cancellation and postponement of numerous propane events, including trade shows was adversely impacting the industry.</p>
                                                            <p>The Virtual Propane Expo is a project designed to unite Exhibitor/Suppliers to at least in part “fill the void” created by virus and together support and serve the industry. With over 125 supplier/exhibitor/speaker participants the feedback we are receiving from the industry has been overwhelmingly positive. ​Participation is free. Premium Service are available.</p>

