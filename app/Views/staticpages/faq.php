                                                <span class="mkdf-tab-title">Who is involved with the launch of VPE?</span>
                                            </h5>
                                            <div class="mkdf-accordion-content">
                                                <div class="mkdf-accordion-content-inner">

                                                    <div class="wpb_text_column wpb_content_element ">
                                                        <div class="wpb_wrapper">
                                                            <p><strong>Bill Stomp</strong> and his team from Digital Software Services began researching and developing the concept of the Virtual Propane Expo in early March 2020 when it became clear that the Coronavirus had begun to shut down state association meetings. The premise was always that a strong propane industry needed a strong group of suppliers to support the needs of a vibrant and growing propane marketer force.</p>
                                                            <p><strong>Shane Sweet</strong>, who has served as an Executive for a number of energy trade associations, including the Vermont Fuel Dealers Association, New England Fuel Institute and most recently the New York Propane Gas Association, and <strong>Mike Digiorgio</strong> (President of Propane University, Founder of Silverback consulting and President of the Propane Legal Defense Association) were instrumental in providing advice, counsel and early development of the platform, as well as promotion of the new site to the propane industry.</p>
                                                            <p>Early on <strong>Peter Gilman</strong> (Senior Executive Specializing in Retail, Rail and Over Road Transportation of Propane and Energy Fuels) became involved to aid and assist in the day to day management and operations to help ensure an expeditious and successful launch. We are glad that <strong>Stuart Flatow</strong> (former VP of PERC) agreed to serve in a consultative role with the greater goal to use this venue to serve the industry in a new way that would benefit suppliers, marketers, associations and propane users.</p>

